    private void saveNewThemeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveNewThemeButtonActionPerformed
        Color col = JColorChooser.showDialog(newThemeFrame, "Choose Theme Color", Color.yellow);
        Theme theme = new Theme(newThemeText.getText());
        String[] keywords = newKeywordText.getText().split(",");
        for(String s: keywords){
            theme.addKeyword(new Keyword(s));
        }
        theme.setColor(col.getRGB());
        projectThemesArray.add(theme);
        projectListModel.addElement(theme);
        projectThemesList.setModel(projectListModel);
        
        PrintWriter outStream =null;
        //hardcoding file name
        try {
            outStream = new PrintWriter(new FileOutputStream("themes.txt",true));
            outStream.println(theme.getStringForFile());
            outStream.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(AnalyzerWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveNewThemeButtonActionPerformed

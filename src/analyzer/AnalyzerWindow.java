    private ArrayList<Keyword> makeKeywordsFromTextField(String k){
        String[] keys = k.split(",");
        ArrayList<Keyword> toReturn = new ArrayList<Keyword>();
        
        for(String s: keys){
            toReturn.add(new Keyword(s));
        }
        return toReturn;
    }
    private void rewriteThemesFile() throws FileNotFoundException{
        PrintWriter outStream = new PrintWriter("themes.txt");
        for(Theme t: projectThemesArray)
            outStream.println(t.getStringForFile());
        outStream.close();
        
    }

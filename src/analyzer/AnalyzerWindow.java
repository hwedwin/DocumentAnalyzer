    private void editThemeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editThemeButtonActionPerformed
        if(projectThemesList.getSelectedValue()!=null){
             int index=projectThemesArray.indexOf(projectThemesList.getSelectedValue());
             final Theme t = projectThemesArray.get(index);
             
             editThemeText.setForeground(t.getColor());
             editKeywordsText.setForeground(t.getColor());
             editThemeText.setText(t.getName());
             editKeywordsText.setText(t.getStringOfKeywords());
             
             editThemeFrame.setVisible(true);
             
             saveChangesButton.addActionListener(new ActionListener(){
                    public void actionPerformed(ActionEvent e){
                           t.setName(editThemeText.getText());
                           t.setKeywords(makeKeywordsFromTextField(editKeywordsText.getText()));
                           Color c = JColorChooser.showDialog(null, "Choose a new color", t.getColor());
                           t.setColor(c.getRGB());
                        try {
                            rewriteThemesFile();
                        } catch (FileNotFoundException ex) {
                            Logger.getLogger(AnalyzerWindow.class.getName()).log(Level.SEVERE, null, ex);
                            }   
                        }
                }
             );
        }
    }//GEN-LAST:event_editThemeButtonActionPerformed


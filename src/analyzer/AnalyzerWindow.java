    private void projectThemesListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_projectThemesListValueChanged
        if(projectThemesList.getSelectedValue()!=null){
            int index=projectThemesArray.indexOf(projectThemesList.getSelectedValue());
            Theme t = projectThemesArray.get(index);
            themeField.setForeground(t.getColor());
            keywordField.setForeground(t.getColor());
            themeField.setText(t.getName());
            keywordField.setText(t.getStringOfKeywords());
        }
        else{
            themeField.setText("");
            keywordField.setText("");
        }
        
    }//GEN-LAST:event_projectThemesListValueChanged
    
    private void deleteThemeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteThemeButtonActionPerformed
        
        if(projectThemesList.getSelectedValue()!=null){
             int index=projectThemesArray.indexOf(projectThemesList.getSelectedValue());
             Theme t = projectThemesArray.get(index);
             projectThemesArray.remove(t);
        }
            PrintWriter outStream = null;
            try {
                outStream = new PrintWriter("themes.txt");
                projectListModel = new DefaultListModel();
                for(Theme th: projectThemesArray){
                    outStream.println(th.getStringForFile());
                    projectListModel.addElement(th);
                }
                outStream.close();
                projectThemesList.setModel(projectListModel);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(AnalyzerWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_deleteThemeButtonActionPerformed


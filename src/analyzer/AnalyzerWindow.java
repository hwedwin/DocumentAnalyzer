    private void selectThemeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectThemeButtonActionPerformed

        int[] indices = projectThemesList.getSelectedIndices();
        ArrayList<Theme> toAdd = new ArrayList<Theme>();
        selectedListModel = new DefaultListModel();
        
        for(int i=0;i<indices.length;i++){
            //We have to use this convuluted statement to get the index of the theme
            //we want because we can't cast an object to a theme
            int index = projectThemesArray.indexOf(projectThemesList.getModel().getElementAt(i));
            Theme t = projectThemesArray.get(index);
            toAdd.add(t);
            selectedListModel.addElement(t);
        }
        selectedThemesList.setModel(selectedListModel);
    }//GEN-LAST:event_selectThemeButtonActionPerformed

